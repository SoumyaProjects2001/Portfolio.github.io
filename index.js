$(document).ready(function(){
    $(".h1").mouseenter(function(){
        $(".P1").fadeIn(400).delay(600).removeClass("none");
        $(".P1").fadeIn(400).delay(600).addClass("block");
    });
});

$(document).ready(function(){
    $(".h1").mouseleave(function(){
        $(".P1").fadeOut(400).delay(600).removeClass("block");
        $(".P1").fadeOut(400).delay(600).addClass("none");
    });
});

